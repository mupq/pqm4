// 辅助函数：快速模乘 (a * b * R^-1 mod Q)
// static inline 配合头文件里的 inline，确保极致内联
static inline int16_t fqmul(int16_t a, int16_t b) {
  return montgomery_reduce((int32_t)a * b);
}

// -------------------------------------------------------------------------
// 正向 NTT (Cooley-Tukey Butterfly)
// -------------------------------------------------------------------------
void ntt(int16_t r[256]) {
  unsigned int len, start, j, k;
  int16_t t, zeta;

  k = 1;
  // 7层循环，步长从 128 减半到 2
  for(len = 128; len >= 2; len >>= 1) {
    for(start = 0; start < 256; start = j + len) {
      zeta = zetas[k++];
      for(j = start; j < start + len; j++) {
        // 蝴蝶运算核心
        t = fqmul(zeta, r[j + len]);
        r[j + len] = r[j] - t;
        r[j] = r[j] + t;
      }
    }
  }
}

// -------------------------------------------------------------------------
// 逆向 NTT (Gentleman-Sande Butterfly)
// -------------------------------------------------------------------------
void invntt(int16_t r[256]) {
  unsigned int start, len, j, k;
  int16_t t, zeta;
  const int16_t f = 1441; // 128^-1 * R^2 mod Q (用于最后的归一化)

  k = 127;
  for(len = 2; len <= 128; len <<= 1) {
    for(start = 0; start < 256; start = j + len) {
      zeta = zetas[k--];
      for(j = start; j < start + len; j++) {
        // 逆向蝴蝶运算 (注意加减顺序与 NTT 相反)
        t = r[j];
        r[j] = barrett_reduce(t + r[j + len]);
        r[j + len] = r[j + len] - t;
        r[j + len] = fqmul(zeta, r[j + len]);
      }
    }
  }

  // 最后的乘法：乘以 1/128 并转换回普通域
  for(j = 0; j < 256; j++)
    r[j] = fqmul(r[j], f);
}

// -------------------------------------------------------------------------
// 基乘法 (在 NTT 域中的点乘)
// -------------------------------------------------------------------------
// 计算 (a0 + a1*X) * (b0 + b1*X) mod (X^2 - zeta)
void basemul(int16_t r[2], const int16_t a[2], const int16_t b[2], int16_t zeta)
{
  r[0]  = fqmul(a[1], b[1]);
  r[0]  = fqmul(r[0], zeta);
  r[0] += fqmul(a[0], b[0]);
  
  r[1]  = fqmul(a[0], b[1]);
  r[1] += fqmul(a[1], b[0]);
}